  cell_type_to_drop <- processed_md_sample %>% 
    group_by(V2) %>% 
    summarise(cell_counts = n()) %>% 
    filter(cell_counts < 5)
  
  processed_md_sample_filt <- processed_md_sample %>% 
    filter(!V2%in%cell_type_to_drop$V2)
  
  ## Rename the tumor cells to identify the cell state in infercnv.
  processed_md_sample_filt <- processed_md_sample_filt[ , c("V1", "V2")]
  
  ## Write formatted annotation table to file
  write.table(processed_md_sample_filt, 
