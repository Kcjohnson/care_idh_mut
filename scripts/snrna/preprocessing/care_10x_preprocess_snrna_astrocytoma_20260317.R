### Harmony batch correction **with set.seed(42)**
astro_sobj_all_harmony <- NormalizeData(seurat_obj_singlets_noncodel, normalization.method = "LogNormalize", scale.factor = 10000)
astro_sobj_all_harmony <- FindVariableFeatures(astro_sobj_all_harmony, selection.method = "vst", nfeatures = 5000)
# By default only variable features are scaled.
astro_sobj_all_harmony <- ScaleData(astro_sobj_all_harmony)
astro_sobj_all_harmony <- RunPCA(astro_sobj_all_harmony, features = VariableFeatures(object = astro_sobj_all_harmony), npcs=50, seed.use = 42)
set.seed(42)
astro_sobj_all_harmony <- RunHarmony(astro_sobj_all_harmony, group.by.vars = c("case_barcode", "lab"), dims.use = 1:30)
astro_sobj_all_harmony <- FindNeighbors(astro_sobj_all_harmony, reduction = "harmony", dims = 1:20) 
# default: random.seed = 0
astro_sobj_all_harmony <- FindClusters(astro_sobj_all_harmony, resolution = 0.6, reduction = "harmony", random.seed = 0) 
#  default: seed.use = 42 
astro_sobj_all_harmony <- RunUMAP(astro_sobj_all_harmony, reduction = "harmony", dims = 1:20, min.dist = 0.2, n.neighbors = 40, seed.use = 42)

